package day08ternaryswitch;

public class Loops01 {
    public static void main(String[] args) {

        //Note: When we need to do repeated actions in java we use loops

        //Type hi 5 times on the console
        //1.Way:
        System.out.println("Hi");
        System.out.println("Hi");
        System.out.println("Hi");
        System.out.println("Hi");
        System.out.println("Hi");
        //To do repeated actions typing repeated code is not acceptable
        // because repeated codes are not good at update at fix and at type

        //2.Way: There are 4 loops in java 1) for-loop 2) while-loop 3) do-while-loop 4)for-eah-loop
        //Starting Value Condition Increment/Decrement
        for(   int i=1    ; i<6   ; i=i+1    ){
            System.out.println("Hi");
        }




    }
}
